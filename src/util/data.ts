import csvParse from 'csv-parse/lib/sync';
import {readFileSync} from 'fs';
import glob from 'glob';

const CPIU_DATA = csvParse(readFileSync("cpiu.csv", {encoding: "utf-8"}), {
    columns: true,
    skip_empty_lines: true
}).reduce((result, currentValue) => {
        result[currentValue["Year"]] = currentValue;
        return result;
    },
    {}
);

function inflation(fromYear, ammount, toYear, toMonth) {
    return ammount / CPIU_DATA[fromYear][toMonth] * CPIU_DATA[toYear][toMonth];
}

