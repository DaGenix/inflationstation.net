RAW_DATA.data.sort((a, b) => {
    if (a.year != b.year) {
        return a.year - b.year;
    } else {
        return a.name.localeCompare(b.name);
    }
});

export interface DataItemType {
    type: "home" | "handheld" | "hybrid",
    raw_orig_price: number,
    name: string,
    manufacturer: string,
    year: number,
    img: string,
    link: string,
    raw_price: number,
    price: string,
    orig_price: string,
    img200Webp: string,
    img400Webp: string,
    img600Webp: string,
    img800Webp: string,
    img1600Webp: string,
    img200Jpeg: string,
    img400Jpeg: string,
    img600Jpeg: string,
    img800Jpeg: string,
    img1600Jpeg: string,
}

export interface DataType {
    inflation_year: number,
    inflation_month: string,
    data: DataItemType[],
}

